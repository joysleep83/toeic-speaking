// 긴 키 우선 매칭을 위해 길이 내림차순 정렬
const _JP_ENTRIES = Object.entries(JP_FURIGANA_MAP)
  .sort((a, b) => b[0].length - a[0].length);

// 단일 정규식 (alternation 앞쪽이 먼저 매칭 → 복합어 우선)
const _JP_RE = new RegExp(
  _JP_ENTRIES.map(([k]) => k.replace(/[.*+?^${}()|[\]\\]/g, '\\$&')).join('|'),
  'g'
);

const _KANJI_RE = /[一-鿿㐀-䶿豈-﫿]/;
const _SPLIT_RE = /[一-鿿㐀-䶿豈-﫿]+|[^一-鿿㐀-䶿豈-﫿]+/g;

/**
 * 한자 부분만 <ruby>로 감싸고 히라가나(okurigana)는 루비 밖에 그대로 둡니다.
 * 예) buildRuby('住んで','すんで') → '<ruby>住<rt>す</rt></ruby>んで'
 *     buildRuby('旅行','りょこう') → '<ruby>旅行<rt>りょこう</rt></ruby>'
 */
function buildRuby(base, reading) {
  const parts = base.match(_SPLIT_RE);
  if (!parts) return `<ruby>${base}<rt>${reading}</rt></ruby>`;

  let result = '';
  let ri = 0;

  for (let i = 0; i < parts.length; i++) {
    const part = parts[i];
    if (!_KANJI_RE.test(part)) {
      // 히라가나·기타: 루비 없이 그대로 출력, 읽기 위치도 같은 만큼 전진
      result += part;
      ri += part.length;
    } else {
      // 한자 블록: 다음 비한자 구간이 reading에서 시작되는 위치를 찾아 읽기 추출
      const next = parts[i + 1];
      let kanjiReading;
      if (!next) {
        kanjiReading = reading.slice(ri);
      } else {
        const idx = reading.indexOf(next, ri);
        if (idx === -1) {
          kanjiReading = reading.slice(ri);
          ri = reading.length;
        } else {
          kanjiReading = reading.slice(ri, idx);
          ri = idx;
        }
      }
      result += `<ruby>${part}<rt>${kanjiReading}</rt></ruby>`;
    }
  }
  return result;
}

/**
 * HTML 문자열에서 태그 내부가 아닌 텍스트 노드에만 후리가나 <ruby> 태그를 삽입합니다.
 * HTML 속성값은 건드리지 않습니다.
 */
function applyJpFurigana(html) {
  return html.replace(/(<[^>]+>)|([^<]+)/g, (match, tag, text) => {
    if (tag) return tag;
    if (!text) return match;
    return text.replace(_JP_RE, (m) => {
      const r = JP_FURIGANA_MAP[m];
      return r ? buildRuby(m, r) : m;
    });
  });
}
